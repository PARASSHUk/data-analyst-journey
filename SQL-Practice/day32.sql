-- SQL COMMANDS
-- DDL = CREATE DROP ALTER
-- DML = INSERT UPDATE DELETE
-- DQL = SELECT

-- CLAUSES IN CORRECT ORDER
-- SELECT FROM WHERE
-- GROUP BY HAVING
-- ORDER BY LIMIT OFFSET

-- OPERATORS
-- Comparison  = > < >= <= !=
-- Logical     = AND OR NOT
-- Special     = IN BETWEEN LIKE
--               IS NULL IS NOT NULL

-- CONSTRAINTS
-- PRIMARY KEY  = unique and not null
-- FOREIGN KEY  = links two tables
-- UNIQUE       = no duplicate values
-- NOT NULL     = cannot be empty
-- DEFAULT      = sets default value
-- CHECK        = must meet condition

-- AGGREGATE FUNCTIONS
-- COUNT SUM AVG MAX MIN
-- COUNT DISTINCT

-- STRING FUNCTIONS
-- UPPER LOWER LENGTH
-- LEFT RIGHT SUBSTRING
-- TRIM REVERSE

-- LOGICAL FUNCTIONS
-- IF IFNULL NULLIF
-- COALESCE CASE WHEN

-- DATE TIME FUNCTIONS
-- CURRENT_DATE NOW DAY
-- MONTH YEAR HOUR MINUTE
-- DATE_ADD DATE_SUB DATEDIFF

-- NUMERIC FUNCTIONS
-- ABS CEIL FLOOR
-- MOD ROUND POWER

-- WINDOW FUNCTIONS
-- ROW_NUMBER RANK DENSE_RANK
-- NTILE LAG LEAD PARTITION BY

-- JOINS
-- INNER JOIN = matching rows only
-- LEFT JOIN  = all left matching right
-- RIGHT JOIN = all right matching left
-- FULL JOIN  = all rows both tables

-- SUBQUERIES
-- Single Row  = one value   use = > 
-- Multi Row   = many values use IN
-- Correlated  = inside SELECT clause

-- CTE
-- WITH name AS (query)
-- SELECT * FROM name
-- Temporary during query only
-- Makes complex queries readable

-- NORMALIZATION
-- 1NF = atomic values no repeating groups
-- 2NF = no partial key dependencies
-- 3NF = no transitive dependencies

-- EDA CONCEPTS
-- DIMENSION = text columns for grouping
-- MEASURE   = numeric columns for calculating
-- INFORMATION_SCHEMA.TABLES  = all tables
-- INFORMATION_SCHEMA.COLUMNS = all columns

