-- Goal:
-- Calculate key metrics of the business
-- Find Big Numbers at highest aggregation
-- Understand the scale of the business

-- Find Total Sales
SELECT
    SUM(sales_amount) AS total_sales
FROM sales;

-- Find Total Items Sold
SELECT
    SUM(quantity) AS total_items_sold
FROM sales;

-- Find Average Selling Price
SELECT
    AVG(price) AS avg_selling_price
FROM sales;

-- Find Total Number of Orders
-- COUNT includes duplicates
SELECT
    COUNT(order_number) AS total_orders
FROM sales;

-- COUNT DISTINCT removes duplicates
SELECT
    COUNT(DISTINCT order_number) AS total_unique_orders
FROM sales;

-- Find Total Number of Products
SELECT
    COUNT(product_key)          AS total_products,
    COUNT(DISTINCT product_key) AS total_unique_products
FROM products;

-- Find Total Number of Customers
SELECT
    COUNT(customer_key) AS total_customers
FROM customer;

-- Find Customers Who Placed Orders
-- Only customers in sales table
SELECT
    COUNT(DISTINCT customer_key) AS customers_with_orders
FROM sales;

-- ------------------------------------------------
-- SQL TASK - Business Key Metrics Report
-- ------------------------------------------------

-- Generate report showing all key metrics
-- Using UNION ALL to combine all metrics
SELECT 'Total Sales'            AS measure_name,
       SUM(sales_amount)        AS measure_value
FROM sales
UNION ALL
SELECT 'Total Quantity',
       SUM(quantity)
FROM sales
UNION ALL
SELECT 'Average Price',
       AVG(price)
FROM sales
UNION ALL
SELECT 'Total Orders',
       COUNT(DISTINCT order_number)
FROM sales
UNION ALL
SELECT 'Total Products',
       COUNT(product_key)
FROM products
UNION ALL
SELECT 'Total Customers',
       COUNT(customer_key)
FROM customer;

-- UNION ALL Rule Learned Today:
-- Number of columns must match
-- Data types of columns must match
-- UNION ALL keeps all rows including duplicates
-- UNION removes duplicates

-- ================================================
-- STEP 5 - MAGNITUDE ANALYSIS
-- ================================================

-- Goal:
-- Compare measure values by categories
-- Understand importance of different categories
-- Formula = [Measure] by [Dimension]
-- Example = Total Sales by Country

-- Find Total Customers by Country
SELECT
    country,
    COUNT(DISTINCT customer_key) AS total_customers
FROM customer
GROUP BY country
ORDER BY total_customers DESC;

-- Find Total Customers by Gender
SELECT
    gender,
    COUNT(customer_key) AS total_customers
FROM customer
GROUP BY gender
ORDER BY total_customers DESC;

-- Find Total Products by Category
SELECT
    category,
    COUNT(product_key) AS total_products
FROM products
GROUP BY category
ORDER BY total_products DESC;

-- Find Average Cost in Each Category
SELECT
    category,
    AVG(cost) AS avg_cost
FROM products
GROUP BY category
ORDER BY avg_cost DESC;

-- Find Total Revenue Generated by Each Category
SELECT
    p.category,
    SUM(s.sales_amount) AS total_revenue
FROM sales s
LEFT JOIN products p
ON p.product_key = s.product_key
GROUP BY p.category
ORDER BY total_revenue DESC;

-- Find Total Revenue Generated by Each Customer
SELECT
    c.customer_key,
    c.first_name,
    c.last_name,
    SUM(s.sales_amount) AS total_revenue
FROM customer c
LEFT JOIN sales s
ON c.customer_key = s.customer_key
GROUP BY
    c.customer_key,
    c.first_name,
    c.last_name
ORDER BY total_revenue DESC;

-- Find Distribution of Sold Items by Country
SELECT
    country,
    SUM(quantity) AS total_sold_items
FROM customer c
LEFT JOIN sales s
ON c.customer_key = s.customer_key
GROUP BY country
ORDER BY total_sold_items DESC;

