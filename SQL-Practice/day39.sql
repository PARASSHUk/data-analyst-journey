-- ================================================
-- STEP 6 - RANKING ANALYSIS
-- ================================================

-- Goal:
-- Order dimension values by measure
-- Find Top N and Bottom N performers
-- Formula = Rank [Dimension] by [Measure]

-- Method 1 - Using TOP keyword
-- TOP keyword in SQL Server = LIMIT in MySQL

-- Find Top 5 Products by Highest Revenue
SELECT TOP 5
    p.product_name,
    SUM(s.sales_amount) AS total_revenue
FROM sales s
LEFT JOIN products p
ON s.product_key = p.product_key
GROUP BY p.product_name
ORDER BY total_revenue DESC;

-- Find 5 Worst Performing Products
SELECT TOP 5
    p.product_name,
    SUM(s.sales_amount) AS total_revenue
FROM sales s
LEFT JOIN products p
ON s.product_key = p.product_key
GROUP BY p.product_name
ORDER BY total_revenue ASC;

-- Find Top 5 Best Performing Subcategories
SELECT TOP 5
    p.subcategory,
    SUM(s.sales_amount) AS total_revenue
FROM sales s
LEFT JOIN products p
ON s.product_key = p.product_key
GROUP BY p.subcategory
ORDER BY total_revenue DESC;

-- ------------------------------------------------
-- Method 2 - Using Window Functions
-- More flexible with extra details
-- ------------------------------------------------

-- Find Top 5 Products using ROW_NUMBER
SELECT *
FROM
(
    SELECT
        p.product_name,
        SUM(s.sales_amount)     AS total_revenue,
        ROW_NUMBER() OVER(
            ORDER BY SUM(s.sales_amount) DESC
        )                       AS rank_products
    FROM sales s
    LEFT JOIN products p
    ON s.product_key = p.product_key
    GROUP BY p.product_name
) t                             -- alias t is required in SQL Server
WHERE rank_products <= 5;       -- every subquery needs an alias

-- Important Note Learned Today:
-- In SQL Server every derived table
-- subquery in FROM clause MUST have alias
-- Without alias SQL Server gives error
-- alias t after closing bracket is required

-- Find Top 10 Customers by Revenue
SELECT TOP 10
    c.customer_key,
    c.first_name,
    c.last_name,
    SUM(s.sales_amount) AS total_revenue
FROM sales s
LEFT JOIN customer c
ON c.customer_key = s.customer_key
GROUP BY
    c.customer_key,
    c.first_name,
    c.last_name
ORDER BY total_revenue DESC;

-- Find Top 3 Customers with Fewest Orders
SELECT TOP 3
    c.customer_key,
    c.first_name,
    c.last_name,
    COUNT(DISTINCT order_number) AS total_orders
FROM sales s
LEFT JOIN customer c
ON c.customer_key = s.customer_key
GROUP BY
    c.customer_key,
    c.first_name,
    c.last_name
ORDER BY total_orders ASC;

