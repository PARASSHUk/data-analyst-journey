## SQL Revision Today

### Commands Revised
- DDL = CREATE DROP ALTER
- DML = INSERT UPDATE DELETE
- DQL = SELECT

### Clauses Revised
- SELECT FROM WHERE
- GROUP BY HAVING
- ORDER BY LIMIT OFFSET

### Functions Revised
- Aggregate  = SUM AVG MAX MIN COUNT
- String     = UPPER LOWER LENGTH LEFT RIGHT TRIM
- Logical    = IF IFNULL CASE WHEN COALESCE
- Date Time  = CURRENT_DATE NOW DAY MONTH YEAR DATEDIFF
- Numeric    = ABS CEIL FLOOR MOD ROUND POWER
- Window     = ROW_NUMBER RANK DENSE_RANK LAG LEAD

### Concepts Revised
- JOINS = INNER LEFT RIGHT FULL
- Subqueries = Single Row Multi Row Correlated
- CTE = Simple and Multiple
- Window Functions with PARTITION BY
- Normalization = 1NF 2NF 3NF
- EDA = Dimension and Measure

